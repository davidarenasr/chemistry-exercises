# A piece of metallic silver (Ag) weighing 194.3 g is placed in a graduated cylinder containing 
# 242 ml of water. The reading in the graduated cylinder is now 260.5 ml. 
# Calculate the density of silver with this data.

weigth = 193.3 
graduate_cylinder = 242 
cylinder_piece_silver = 260.5
volume_piece = cylinder_piece_silver - graduate_cylinder
density = weigth / volume_piece

print ("the density is " + str(density) + "gr/ml")